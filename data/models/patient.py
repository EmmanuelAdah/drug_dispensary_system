class Patient:
    def __init__(self, patient_id, first_name, last_name, gender, birth_date, email, phone):
        self.patient_id = patient_id
        self.first_name = first_name
        self.last_name = last_name
        self.gender = gender
        self.birth_date = birth_date
        self.email = email
        self.phone = phone
