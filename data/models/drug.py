class Drug:
    def __init__(self, id, name, type, category, manufacture_date, expiry_date, date_added, quantity):
        self.drug_id = id
        self.name = name
        self.type = type
        self.category = category
        self.manufacture_date = manufacture_date
        self.expiry_date = expiry_date
        self.date_added = date_added
        self.quantity = quantity

